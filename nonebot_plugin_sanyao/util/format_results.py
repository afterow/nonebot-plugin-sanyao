

def format_results(results):
    """将结果格式化为所需的字符串形式"""
    formatted_string = (
        f'"{results[5]}", "{results[6]}", "{results[7]}", "{results[8]}",\n'
        f'"{results[4]}", "{results[12]}", "{results[13]}", "{results[9]}",\n'
        f'"{results[3]}", "{results[14]}", "{results[15]}", "{results[10]}",\n'
        f'"{results[2]}", "{results[1]}", "{results[0]}", "{results[11]}"'
    )
    return formatted_string



